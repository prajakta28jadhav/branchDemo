package com.java.methods;

public class ReturnTypeWithNoParameter {
	int a= 50,b=60;
	public int add(){
		int c = a+b;
		return c;
			
	}
	public int sub(){
		int c = a-b;
		return c;
			
	}
	public int mul(){
		int c = a*b;
		return c;
			
	}
	public float div(){
		float c = (float)a/b;
		return c;
			
	}
}
